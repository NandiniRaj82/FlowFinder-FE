// ──────────────────────────────────────────────────────────────────────────
// Main Results Component
// ──────────────────────────────────────────────────────────────────────────
const WebsiteRedesignerResults: React.FC<Props> = ({
  designs, websiteUrl, pageTitle, screenshotBase64, stats, onReset, isStreaming, pendingStyles = [],
}) => {
  const [openDesign, setOpenDesign] = useState<Design | null>(null);

  const hostname = (() => { try { return new URL(websiteUrl).hostname; } catch { return websiteUrl; } })();

  return (
    <>
      <style>{`
        @keyframes slideUp{from{opacity:0;transform:translateY(20px)}to{opacity:1;transform:translateY(0)}}
        @keyframes shimmer{0%{background-position:200% 0}100%{background-position:-200% 0}}
        @keyframes spin{to{transform:rotate(360deg)}}
        @keyframes pulse{0%,100%{opacity:1}50%{opacity:0.5}}
      `}</style>

      {openDesign && <PreviewModal design={openDesign} onClose={() => setOpenDesign(null)} />}

      <div style={{ maxWidth: 1200, margin: '0 auto', padding: '32px 24px' }}>

        {/* ── Header ── */}
        <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: 28 }}>
          <div style={{ display: 'flex', alignItems: 'center', gap: 14 }}>
            {screenshotBase64 && (
              <img
                src={`data:image/jpeg;base64,${screenshotBase64}`}
                style={{ width: 64, height: 44, objectFit: 'cover', borderRadius: 8, border: '1.5px solid #e2e8f0' }}
                alt="screenshot"
              />
            )}
            <div>
              <p style={{ margin: 0, fontSize: 12, color: '#94a3b8', fontFamily: 'monospace' }}>{hostname}</p>
              <h2 style={{ margin: '2px 0 0', fontSize: 22, fontWeight: 800, color: '#0f172a' }}>
                {pageTitle || hostname}
              </h2>
              <p style={{ margin: '2px 0 0', fontSize: 12, color: '#64748b' }}>
                {isStreaming
                  ? `${designs.length} ready · ${pendingStyles.length} generating…`
                  : `${designs.length} design${designs.length !== 1 ? 's' : ''} generated`}
              </p>
            </div>
          </div>

          <button
            onClick={onReset}
            style={{
              display: 'flex', alignItems: 'center', gap: 6,
              padding: '8px 16px', borderRadius: 10,
              border: '1.5px solid #e2e8f0', background: '#fff',
              fontSize: 13, fontWeight: 700, color: '#475569',
              cursor: 'pointer', fontFamily: 'inherit',
              transition: 'all 0.15s',
            }}
          >
            ← New Redesign
          </button>
        </div>

        {/* ── Stats ── */}
        {stats && (
          <div style={{
            display: 'flex', flexWrap: 'wrap', gap: 8, marginBottom: 28,
            padding: '12px 16px', background: '#f8fafc', borderRadius: 12,
            border: '1px solid #e2e8f0',
          }}>
            {[
              { label: 'Headings', val: stats.headings },
              { label: 'Paragraphs', val: stats.paragraphs },
              { label: 'List Items', val: stats.listItems },
              { label: 'Badges', val: stats.tags },
              { label: 'Sections', val: stats.sections },
            ].map(s => (
              <div key={s.label} style={{
                display: 'flex', alignItems: 'center', gap: 6,
                padding: '4px 12px', borderRadius: 20,
                background: 'rgba(99,102,241,0.08)', border: '1px solid rgba(99,102,241,0.18)',
              }}>
                <span style={{ fontWeight: 800, fontSize: 13, color: '#4f46e5' }}>{s.val}</span>
                <span style={{ fontSize: 11, color: '#64748b' }}>{s.label}</span>
              </div>
            ))}
          </div>
        )}

        {/* ── Grid ── */}
        <div style={{
          display: 'grid',
          gridTemplateColumns: 'repeat(auto-fill, minmax(340px, 1fr))',
          gap: 24,
        }}>
          {designs.map((design, i) => (
            <DesignCard
              key={design.style}
              design={design}
              index={i}
              onClick={() => setOpenDesign(design)}
            />
          ))}
          {pendingStyles.map(s => <SkeletonCard key={s} style={s} />)}
        </div>

      </div>
    </>
  );
};

export default WebsiteRedesignerResults;